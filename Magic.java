public class Magic {
    private final String name;
    private final int magicPower;

    public Magic(String name, int magicPower) {
        this.name = name;
        this.magicPower = magicPower;
    }

    public String getName() {
        return name;
    }

    public int getMagicPower() {
        return magicPower;
    }
}